import tkinter as aps

# Buat jendela aplikasi
aplikasi = aps.Tk()

# Ngasih judul ke jendela
aplikasi.title("Aplikasi Pertamaku")
# Ngasih ukuran jendela
aplikasi.geometry("400x300")      # Ukuran awal
aplikasi.resizable(True, True)    # Boleh di-resize (horizontal & vertikal)


# Menahan jendela tetap muncul
aplikasi.mainloop()

